public class Circle {

    private int radius;
    private int corlor;

    public Circle(int radius, int corlor){
        this.radius = radius;
        this.corlor = corlor;
    }

    public int getRadius() {
        return radius;
    }

    public int getCorlor() {
        return corlor;
    }
}
